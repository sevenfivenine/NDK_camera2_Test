/*** AARelativisticAberration ****************************************************/

double AARelativisticAberration ( double p[3], double v[3], double p1[3] )
{
	double beta, dot, r, s, n;
	
	beta = sqrt ( 1.0 - ( v[0] * v[0] + v[1] * v[1] + v[2] * v[2] ) );
	r = sqrt ( p[0] * p[0] + p[1] * p[1] + p[2] * p[2] );
  
	if ( r > 0.0 )
		dot = ( p[0] * v[0] + p[1] * v[1] + p[2] * v[2] ) / r;
	else
		dot = 0.0;
  	
	s = r * ( 1.0 + dot / ( 1.0 + beta ) );
	n = 1.0 + dot;
	
	p1[0] = ( p[0] * beta + v[0] * s ) / n;
	p1[1] = ( p[1] * beta + v[1] * s ) / n;
	p1[2] = ( p[2] * beta + v[2] * s ) / n;
  
  return ( ( 1.0 - dot ) / beta );
}

#define TOLERANCE 1.0e-8

/*** AAGeocentricVelocity **********************************************************/

void AAGeocentricVelocity ( double l, double p, double h, double a, double f, double w,
double *x, double *y, double *z )
{
	double c, s, cp = cos ( p ), sp = sin ( p );
	
	f = ( 1.0 - f ) * ( 1.0 - f );
	c = 1.0 / sqrt ( cp * cp + f * sp * sp );
	s = f * c;
	
	*x = -w * ( a * c + h ) * cp * cos ( l );
	*y =  w * ( a * c + h ) * cp * sin ( l );
	*z = 0.0;
}

/*** AAGeodeticToGeocentricXYZ *****************************************************/
      
void AAGeodeticToGeocentricXYZ ( double l, double p, double h, double a, double f,
double *x, double *y, double *z )
{
	double c, s, cp = cos ( p ), sp = sin ( p );

	f = ( 1.0 - f ) * ( 1.0 - f );
	c = 1.0 / sqrt ( cp * cp + f * sp * sp );
	s = f * c;
	
	*x = ( a * c + h ) * cp * cos ( l );
	*y = ( a * c + h ) * cp * sin ( l );
	*z = ( a * s + h ) * sp;
}

/*** AAGeocentricXYZToGeodetic ******************************************************/

void AAGeocentricXYZToGeodetic ( double x, double y, double z, double a, double f,
double *l, double *p, double *h )
{
	double r, e2, p1, s, c;
	
	*l = atan2pi ( y, x );

	r = sqrt ( x * x + y * y );
	e2 = 2.0 * f - f * f;

	if ( r > 0.0 )
	{
		*p = atan ( z / r );
		do
		{
			p1 = *p;
			s = sin ( p1 );
			c = 1.0 / sqrt ( 1.0 - e2 * s * s );
			*p = atan ( ( z + a * c * e2 * s ) / r );
		}
  		while ( fabs ( p1 - *p ) > TOLERANCE );
  	}
  	else
  	{
  		if ( z > 0.0 )
  		{
  			*p = HALF_PI;
  			c = 1.0 / ( 1.0 - f );
  		}
  		else if ( z < 0.0 )
  		{
  			*p = -HALF_PI;
  			c = 1.0 / ( 1.0 - f );
  		}
  		else
  		{
  			*p = 0.0;
  			c = 1.0;
  		}
  	}
  	
	*h = r / cos ( *p ) - a * c;
}

/*** AARefractionAngle **********************************************************/

double AARefractionAngle ( double h, double p, double t, int a )
{
	double	r;
	
	if ( a == true )
	{
		h = MAX ( RAD_TO_DEG ( h ), -1.9 );
		r = ARCMIN_TO_RAD ( 1.02 / tan ( DEG_TO_RAD ( h + ( 10.3 / ( h + 5.11 ) ) ) ) );
	}
	else
	{
		h = MAX ( RAD_TO_DEG ( h ), -1.7 );
		r = ARCMIN_TO_RAD ( 1.0 / tan ( DEG_TO_RAD ( h + ( 7.31 / ( h + 4.4 ) ) ) ) );
	}
	
	r = r * ( p / 1010.0 ) * ( 283.0 / ( 273.0 + t ) );
	return ( r );
}

/*** AAHorizonToEquatorial ******************************************************/

void AAHorizonToEquatorial ( double azm, double alt, double lon, double lat, double jd, double *ra, double *dec )
{
	azm -= PI;	// these formulae assume azimuth is measured from south.  Our convention is 0 = north.
	
	double	ha = 0.0, lst = AAGreenwichMeanSiderealTime ( jd ) + lon;
	double	sinalt = sin ( alt ), cosalt = cos ( alt );
	double	sinazm = sin ( azm ), cosazm = cos ( azm );
	double	sinlat = sin ( lat ), coslat = cos ( lat );
	
	if ( cosalt == 0.0 )
		ha = lst;
	else
		ha = atan2pi ( sinazm, cosazm * sinlat + sinalt * coslat / cosalt );
	
	*ra = Mod2Pi ( lst - ha );
	*dec = asin ( sinlat * sinalt - coslat * cosalt * cosazm );
}

/*** AAEquatorialToHorizon ******************************************************/

void AAEquatorialToHorizon ( double ra, double dec, double lon, double lat, double jd, double *azm, double *alt )
{
	double	ha = AAGreenwichMeanSiderealTime ( jd ) + lon - ra;
	double	cosha = cos ( ha ), sinha = sin ( ha );
	double	sinlat = sin ( lat ), coslat = cos ( lat );
	double	sindec = sin ( dec ), cosdec = cos ( dec );
	
	if ( cosdec == 0.0 )
		*azm = 0.0;
	else
		*azm = Mod2Pi ( atan2 ( sinha, cosha * sinlat - sindec * coslat / cosdec ) + PI );	// see above comment re: azimuth
	
	*alt = asin ( sinlat * sindec + coslat * cosdec * cosha );
}

/*** AASetPrecessionRotationMatrix ************************************************/

void AASetPrecessionRotationMatrix ( double m[3][3], double jd0, double jd1, int n )
{
	double zeta, z, theta, e, dl, de;

	AAPrecession ( jd0, jd1, &zeta, &z, &theta, NULL, NULL, NULL );
	
	if ( n )
	{
		e = AAObliquity ( jd1 );
		AANutation ( jd1, &dl, &de );
		AASetRotationMatrix ( m, 6, 2, zeta, 1, theta, 2, z, 0, -e, 2, dl, 0, e + de );
	}
	else  
	{
		AASetRotationMatrix ( m, 3, 2, zeta, 1, theta, 2, z );
	}
}

/*** AASetNutationRotationMatrix *************************************************/

void AASetNutationRotationMatrix ( double m[3][3], double e, double dl, double de, int s )
{
	if ( s > 0 )
		AASetRotationMatrix ( m, 3, 0, -e, 2, dl, 0, e + de );
	else
		AASetRotationMatrix ( m, 3, 0, -e - de, 2, -dl, 0, e );
}

/******************  SetEclipticPrecessionRotationMatrix  ***********************/

void AASetEclipticPrecessionRotationMatrix ( double m[3][3], double jd0, double jd1 )
{
	double eta, pi, p;

	AAPrecession ( jd0, jd1, NULL, NULL, NULL, &eta, &pi, &p );
	AASetRotationMatrix ( m, 3, 2, -pi, 0, -eta, 2, pi + p );
}

/*** AASetEclipticRotationMatrix ******************************************/

void AASetEclipticRotationMatrix ( double m[3][3], double e, int s )
{
	if ( s > 0 )
		AASetRotationMatrix ( m, 1, 0, -e );
	else
		AASetRotationMatrix ( m, 1, 0, e );
}

/*** AASetGalacticRotationMatrix ***********************************************/

void AASetGalacticRotationMatrix ( double m[3][3], double jd, int s )
{
	double i, n, w, zeta, z, theta;
  
	w = GALACTIC_LON_ASC_NODE * RAD_PER_DEG;
	i = ( 90.0 - GALACTIC_N_POLE_DEC ) * RAD_PER_DEG;
	n = ( 90.0 + GALACTIC_N_POLE_RA  ) * RAD_PER_DEG;
  
	AAPrecession ( jd, B1950, &zeta, &z, &theta, NULL, NULL, NULL );
  
	if ( s > 0 )
		AASetRotationMatrix ( m, 6, 2, zeta, 1, theta, 2, z, 2, -n, 0, -i, 2, w );
	else
		AASetRotationMatrix ( m, 6, 2, -w, 0, i, 2, n, 2, -z, 1, -theta, 2, -zeta );
}                              

/*** AASetHorizonRotationMatrix ***************************************************/

void AASetHorizonRotationMatrix ( double m[3][3], double l, double b, int s )
{
	int i;
  
	if ( s > 0 )
	{
		AASetRotationMatrix ( m, 2, 2, PI - l, 1, b - HALF_PI );
		for ( i = 0; i < 3; i++ )
			m[1][i] = -m[1][i];
	}
	else
	{
		AASetRotationMatrix ( m, 2, 1, HALF_PI - b, 2, l - PI );
		for ( i = 0; i < 3; i++ )
			m[i][1] = -m[i][1];
	}  
}

/*** AASetLongTermPrecessionMatrix **************************************************/

void AASetLongTermPrecessionMatrix ( double m[3][3], double jd0, double jd1, int n )
{
	double chiA, omegaA, psiA, epsilonA, epsilon0, dl = 0, de = 0, m0[3][3], m1[3][3];

	/*** If we are correcting for nutation, compute the nutation angles
	     at the final epoch. ***/
	     
	if ( n )
		AANutation ( jd1, &dl, &de );

	/*** Compute obliquity of ecliptic at J2000, in radians. ***/
	
	epsilon0 = AADegMinSecToDecimal ( 23, 26, 21.448, '+' ) / DEG_PER_RAD;
	
	/*** If initial epoch is not J2000, create rotation matrix for
	     transforming from initial epoch to J2000. ***/
	     
	if ( jd0 != J2000 )
	{
		AALongTermPrecession ( jd0, &chiA, &omegaA, &psiA, &epsilonA );
		AASetRotationMatrix ( m0, 4, 2, chiA, 0, -omegaA, 2, -psiA, 0, epsilon0 );
	}
	else
	{
		AASetIdentityRotationMatrix ( m0 );
	}
	
	/*** If final epoch is not J2000, create rotation matrix for
	     transforming from J2000 to final epoch, including (if desired)
	     the correction for nutation. ***/
	     
	if ( jd1 != J2000 )
	{
		AALongTermPrecession ( jd1, &chiA, &omegaA, &psiA, &epsilonA );
		if ( n )
			AASetRotationMatrix ( m1, 7, 0, -epsilon0, 2, psiA, 0, omegaA, 2, -chiA, 0, -epsilonA, 2, dl, 0, epsilonA + de );
		else
			AASetRotationMatrix ( m1, 4, 0, -epsilon0, 2, psiA, 0, omegaA, 2, -chiA );
	}
	else
	{
		if ( n )
			AASetNutationRotationMatrix ( m1, epsilon0, dl, de, 1 );
		else
			AASetIdentityRotationMatrix ( m1 );
	}
	
	/*** Final rotation matrix should transform from initial epoch
	     to final epoch. ***/
	     
	AATransformRotationMatrix ( m1, m0 );
	AACopyRotationMatrix ( m, m0 );
}

